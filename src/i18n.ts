export function getAppLanguage(): AppLanguage {
  const lang = (localStorage.getItem('appLanguage') || 'en') as AppLanguage;
  if (lang === 'de' || lang === 'tr' || lang === 'en') return lang;
  return 'en';
}

export function useI18n() {
  const lang = getAppLanguage();
  const dict = useMemo(() => messages[lang] || messages.en, [lang]);
  const t = useCallback((key: string, fallback?: string) => dict[key] || fallback || key, [dict]);
  return { lang, t };
}
