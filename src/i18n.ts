const LANGUAGE_STORAGE_KEY = 'appLanguage';
const LANGUAGE_EVENT = 'app-language-change';

const isSupportedLanguage = (value: string): value is AppLanguage =>
  value === 'en' || value === 'de' || value === 'tr';

export function getAppLanguage(): AppLanguage {
  if (typeof window === 'undefined') return 'en';
  const lang = localStorage.getItem(LANGUAGE_STORAGE_KEY) || 'en';
  return isSupportedLanguage(lang) ? lang : 'en';
}

export function setAppLanguage(language: AppLanguage) {
  if (typeof window === 'undefined' || !isSupportedLanguage(language)) return;
  localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  window.dispatchEvent(new Event(LANGUAGE_EVENT));
}

function subscribeToLanguageChange(callback: () => void) {
  if (typeof window === 'undefined') {
    return () => undefined;
  }

  const handleStorage = (event: StorageEvent) => {
    if (!event.key || event.key === LANGUAGE_STORAGE_KEY) {
      callback();
    }
  };

  window.addEventListener('storage', handleStorage);
  window.addEventListener(LANGUAGE_EVENT, callback);

  return () => {
    window.removeEventListener('storage', handleStorage);
    window.removeEventListener(LANGUAGE_EVENT, callback);
  };
}

export function useI18n() {
  const lang = useSyncExternalStore(subscribeToLanguageChange, getAppLanguage, () => 'en');
  const dict = useMemo(() => messages[lang] || messages.en, [lang]);

  const t = useCallback(
    (key: string, fallback?: string, values?: Record<string, string | number>) => {
      let template = dict[key] || fallback || key;
      if (values) {
        Object.entries(values).forEach(([token, value]) => {
          template = template.replaceAll(`{{${token}}}`, String(value));
        });
      }
      return template;
    },
    [dict]
  );

  return { lang, t };
}
